package com.syntax.class07;

public class WhileLoops {

	public static void main(String[] args) {

		int num=1;
		while(num<=5) {
			System.out.println("Hello");
			num++;
		}
		System.out.println("-------Using Do While Loop-------");
		int num1=11;
		do {
			System.out.println("Hello");
			num1++;
		}while (num1<=5);
	}

}
